function showPrograms(stream) {
    const contentArea = document.getElementById("contentArea");
    const streamData = data[stream];

    if (!streamData) return;

    // Clear existing content
    contentArea.innerHTML = "";

    // Add Programs Dropdown
    const programsHeading = document.createElement("h3");
    programsHeading.textContent = "Programs";
    contentArea.appendChild(programsHeading);

    const programsDropdown = document.createElement("select");
    programsDropdown.id = "programFilter";
    programsDropdown.innerHTML = `<option value="">Select a Program</option>`; // Default option

    // Populate dropdown with programs
    Object.keys(streamData.programs).forEach((program) => {
        const option = document.createElement("option");
        option.value = program;
        option.textContent = program;
        programsDropdown.appendChild(option);
    });
    contentArea.appendChild(programsDropdown);

    // Add Department Dropdown
    const departmentHeading = document.createElement("h3");
    departmentHeading.textContent = "Departments";
    departmentHeading.style.display = "none"; // Initially hidden
    contentArea.appendChild(departmentHeading);

    const departmentDropdown = document.createElement("select");
    departmentDropdown.id = "departmentFilter";
    departmentDropdown.innerHTML = `<option value="">Select a Department</option>`;
    departmentDropdown.style.display = "none"; // Initially hidden
    contentArea.appendChild(departmentDropdown);

    // Add Faculty Section
    const facultyHeading = document.createElement("h3");
    facultyHeading.textContent = "Faculty";
    contentArea.appendChild(facultyHeading);

    const facultyContainer = document.createElement("div");
    facultyContainer.id = "facultyContainer";
    contentArea.appendChild(facultyContainer);

    // Function to display departments for a selected program
    function displayDepartments(program) {
        departmentDropdown.style.display = "none"; // Hide initially
        departmentHeading.style.display = "none";
        facultyContainer.innerHTML = ""; // Clear faculty section

        const programData = streamData.programs[program];

        if (programData && programData.departments) {
            departmentHeading.style.display = "block";
            departmentDropdown.style.display = "block";

            departmentDropdown.innerHTML = `<option value="">Select a Department</option>`; // Reset options

            Object.keys(programData.departments).forEach((department) => {
                const option = document.createElement("option");
                option.value = department;
                option.textContent = department;
                departmentDropdown.appendChild(option);
            });
        }
    }

    // Function to display faculty for the selected department
    function displayFaculty(department) {
        facultyContainer.innerHTML = ""; // Clear previous faculty

        const selectedProgram = programsDropdown.value;
        if (
            selectedProgram &&
            streamData.programs[selectedProgram].departments[department]
        ) {
            const departmentFaculty =
                streamData.programs[selectedProgram].departments[department];
            departmentFaculty.forEach((faculty) => {
                const facultyCard = document.createElement("div");
                facultyCard.className = "faculty-card";

                const facultyImage = document.createElement("img");
                facultyImage.src = faculty.image;
                facultyImage.alt = `${faculty.name}'s photo`;
                facultyImage.className = "faculty-image";

                const facultyName = document.createElement("p");
                facultyName.textContent = faculty.name;

                facultyCard.appendChild(facultyImage);
                facultyCard.appendChild(facultyName);

                facultyContainer.appendChild(facultyCard);
            });
        } else {
            facultyContainer.innerHTML =
                "<p>Please select a valid department to view faculty.</p>";
        }
    }

    // Event listeners
    programsDropdown.addEventListener("change", (e) => {
        displayDepartments(e.target.value);
    });

    departmentDropdown.addEventListener("change", (e) => {
        displayFaculty(e.target.value);
    });
}
